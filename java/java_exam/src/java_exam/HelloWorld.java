package java_exam;
//class ==> 프로그램의 최소 실행단위

public class HelloWorld {
	
//	메소드 영역
//	public static void main(String[] args): 
//	해당 클래스를 실행 시킬 수 있는 메소드
	public static void main(String[] args) {
		System.out.println("Hello World!!");
		
		System.out.println(3 + 2);
		System.out.println(3 - 2);
		System.out.println(3 * 2);
		System.out.println(3 / 2);
		System.out.println(3 % 2);
		
	}

}
